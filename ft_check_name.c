#include "ft_cub3d.h"

void	check_name(t_mlx *mlx, char *s)
{
	int		i;

	i = 0;
	while (s[i] && s[i] != '.')
		i++;
	if (s[i] && s[i] == '.')
		if (ft_strncmp(&s[i], ".cub\0", 5) != 0)
			error_manager(6, mlx);
}
